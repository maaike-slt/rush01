void	init_grid(int grid[9][9], int n)
{
	int	x;
	int	y;

	x = 0;
	while (x < n)
	{
		y = 0;
		while (y < n)
		{
			grid[x][y] = 0;
			y++;
		}
		x++;
	}
}
